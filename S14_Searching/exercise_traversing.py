### If you know a solution is not far from the root of the tree: BFS

### If solutions are frequent and located deep in the tree: DFS

### Finding the shortest path: BFS

### Determining whether a path exists between two nodes: DFS

### If the tree is very deep and solutions are rare: BFS (DFS would take too much time)

### If the tree is very wide: DFS (BFS would need too much memory)
