### Section 13 | Sorting Exercise
########################################################################################################################

### 1 - Sort 10 schools around your house by distance:
# Insertion Sort (good for small datasets)

### 2 - Sorting eBay listings by the current bid amount:
# Counting or Radix Sort (good for limited number/range of integers)

### 3 - Sorting sport scores on ESPN:
# Quick Sort (good for large/random datasets, main issue is memory)

### 4 - Sorting a massive database (does not fit into memory) of past year's user data:
# Merge Sort (good for large/random datasets, main issue is performance)

### 5 - Adding 2 new reviews and updating a presorted list of Udemy reviews:
# Insertion Sort (good for adding small datasets to a presorted list)

### 6 - Sorting temperature records for the past 50 years in Canada:
# Integers: Counting or Radix Sort (good for limited number/range of integers)
# Decimals: Quick Sort (good for large/random datasets, main issue is memory)

### 7 - Sorting a large user database. Data is very random:
# Quick Sort (good for large/random datasets, main issue is memory)

### 8 - Teaching sorting algorithms for the first time:
# Bubble Sort, Selection Sort (simple, easy to understand algorithms)
