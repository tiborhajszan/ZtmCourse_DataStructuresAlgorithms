### Section 13 | Sorting Exercise
########################################################################################################################

### 1 - Sorting 10 schools around your house by distance:
# Insertion Sort (good for small datasets)

### 2 - Sorting eBay listings by the current bid amount:
# Counting Sort or Radix Sort (good for limited number/range of integers)

### 3 - Sorting sport scores on ESPN:
# Quick Sort (good for large/random datasets, main concern is memory)

### 4 - Sorting a massive database of user data from last year (does not fit into memory):
# Merge Sort (good for large/random datasets, main concern is performance)

### 5 - Adding 2 new reviews to update a presorted list of Udemy reviews:
# Insertion Sort (good for adding a few items to a presorted dataset)

### 6 - Sorting temperature records for the past 50 years in Canada:
# Integers: Counting Sort or Radix Sort (good for limited number/range of integers)
# Decimals: Quick Sort (good for large/random datasets, main concern is memory)

### 7 - Sorting a large user database. Data is very random:
# Quick Sort (good for large/random datasets, main concern is memory)

### 8 - Teaching sorting algorithms for the first time:
# Bubble Sort or Selection Sort (simple, easy to understand algorithms)
